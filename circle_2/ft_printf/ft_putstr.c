#include "ft_printf.h"

size_t	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (!str)
		str = "(null)";
	while (str[i])
	{
		ft_putchar (str[i]);
		i++;
	}
	return (i);
}
